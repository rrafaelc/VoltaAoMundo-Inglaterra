      <p class="lead justify">
        Os primeiros povos a ocuparem a ilha da Grã-Bretanha, onde está
        localizada a Inglaterra, foram os celtas. O domínio estrangeiro sobre
        os territórios britânicos continuou com o Império Romano por volta do
        século I a.C., do qual o país fez parte até pelo menos o início do
        século V da era atual. A partir de então, os anglo-saxões tiveram um
        papel essencial no que tange à conformação do território inglês como
        hoje o conhecemos, influenciando diretamente na sua formação
        sociocultural e política, processo esse que não foi interrompido nem
        mesmo pela invasão dos vikings no século VIII. Disputas políticas e
        territoriais marcaram a história inglesa, culminando em acontecimentos
        como a Guerra dos Cem Anos (1337-1453) e a Guerra das Rosas
        (1455-1487). O século XVI foi caracterizado pela expansão territorial
        inglesa e pela conformação de um reino unificado que agregou
        Inglaterra, País de Gales e a Escócia sob um mesmo sistema político. A
        Irlanda do Norte veio a se juntar somente no início do século XIX,
        formando então o Reino Unido. Nesse ínterim, teve início na Inglaterra
        a Revolução Industrial, que provocou transformações profundas na
        produção e na forma de trabalho em todo o mundo, além de colocar o
        país no centro da economia internacional à época. Junto do Reino
        Unido, a Inglaterra constituiu colônias nos continentes africanos e
        asiático entre os séculos XIX e XX no que ficou conhecido como
        neocolonialismo. Pouco tempo mais tarde, envolveu-se com os conflitos
        das duas guerras mundiais. A Inglaterra é hoje considerado um dos
        países mais desenvolvidos do mundo e um importante centro financeiro
        internacional, tendo papel central na política e economia do Reino
        Unido.
      </p>
